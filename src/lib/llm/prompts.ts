/**
 * Q1 Analysis: 2問目の選択肢生成（軽量）
 */
export function createQ1AnalysisPrompt(q1: string): string {
  return `以下の経営課題入力から、「この判断で最優先で明確にしたい論点」の候補を2〜4個選んでください。

【選択できる論点カテゴリ】
以下の4カテゴリから、入力に照らして最も関連度の高いものを選んでください。

1. 継続・停止の判断基準を明確にしたい（AI投資の継続・見直し・停止の判断軸が定まっていない場合）
2. ROI・成果基準を明確にしたい（期待成果と評価基準のギャップが論点の場合）
3. 最終判断者と責任範囲を明確にしたい（誰が判断するか・誰が責任を負うかが曖昧な場合）
4. 追加投資・拡大判断の基準を明確にしたい（拡大・追加投資の是非が止まっている場合）

【選択方針】
- 入力に明示・示唆されている論点のみ選ぶ
- 2〜4個の範囲で選ぶ（入力に関連するものだけ）
- text はカテゴリ文言をそのまま使うか、入力に合わせて20文字以内で調整してよい

【入力】
${q1}

【出力形式】
Markdown記法は使わず、JSONのみを出力してください。

{
  "options": [
    { "id": "opt_1", "text": "論点カテゴリの記述" },
    { "id": "opt_2", "text": "論点カテゴリの記述" }
  ]
}`;
}

/**
 * Layer 1: 混線検知
 */
export function createLayer1Prompt(inputText: string): string {
  return `あなたは経営判断の構造分析の専門家です。以下の入力テキストに「複数の独立した判断」が混在しているか判定してください。

【重要な判定基準】
1. 「複数の判断基準」は論点の混在ではありません
   - 例：「ROIと実行スピードの両立」→ 1つの判断を2つの基準で評価（混在なし）

2. 「複数の独立した判断」のみを混在と判定してください
   - 異なる判断主体（CEO判断 vs CTO判断）
   - 異なる判断対象（AI導入 vs 人材採用）
   - 異なる時間軸の判断（今月の判断 vs 来期の判断）

3. **依存関係がある場合も複数論点として扱う**
   - 「まず予算確保、それからAI導入」→ 2論点（依存関係あり）
   - 「Aを決めてからBを決める」→ 2論点（前提条件あり）

4. **文脈的つながりが強くても、判断が複数あれば混在**
   - 「AI導入したいが、スキル不足が心配。予算もない。CFOが反対」
     → 複数の判断が混在（AI導入判断 + 人材育成判断 + 予算配分判断）
   - 中心テーマが同一でも、決めるべきことが複数あれば混在

5. 以下は混在ではありません：
   - 1つの判断に複数の評価基準がある
   - 1つの判断に複数の制約条件がある
   - 1つの判断に複数の選択肢がある

【入力テキスト】
${inputText}

【判定例】
- 「社内リソース投入 vs 外部委託、判断基準はROIと実行スピード」→ isMixed: false, issueCount: 1
- 「AI導入を進めたいが、社員のスキル不足が心配。一方で予算が足りない」→ isMixed: true, issueCount: 2
- 「まず予算確保の承認を得てから、AI導入プロジェクトを開始するか決めたい」→ isMixed: true, issueCount: 2（依存関係あり）

【出力形式】
必ず以下のJSON形式で回答してください。Markdown記法は使わず、JSONのみを出力してください。

{
  "isMixed": true or false,
  "reasoning": "判定理由（100文字以内）",
  "issueCount": 推定される独立した判断の数（1以上の整数）
}`;
}

/**
 * Layer 2: 論点分離
 */
export function createLayer2Prompt(inputText: string): string {
  return `あなたは経営判断の構造分析の専門家です。入力テキストから「独立した判断」を抽出してください。

【重要な抽出基準】
1. 「判断」の定義（以下のいずれかが異なる場合のみ別論点）：
   - 判断主体が異なる → 別論点
   - 判断対象が異なる → 別論点
   - 判断期限が異なる → 別論点

2. 「判断」ではないもの（これらは1つの判断内の要素）：
   - 判断基準の複数性（ROI vs 実行スピード）→ 1つの判断の評価軸
   - 選択肢の複数性（A案 vs B案）→ 1つの判断の選択肢
   - 制約条件の複数性（予算・時間・人材）→ 1つの判断の制約

3. 最小論点数の原則：
   - 入力が1つの判断のみの場合、無理に分割しない
   - 判断基準が複数あっても、判断主体・対象・期限が同一なら1論点

【抽出例】
- 「社内リソース投入 vs 外部委託、判断基準はROIと実行スピード」→ 1論点（リソース投入方法の決定）
- 「AI導入を進めたいが、予算が足りない。CFOが難色を示している」→ 2論点（AI導入判断 / 予算配分判断）

【入力テキスト】
${inputText}

【出力形式】
必ず以下のJSON形式で回答してください。Markdown記法は使わず、JSONのみを出力してください。
論点は最大3つまでとしてください（DEMO版制限）。

{
  "issues": [
    {
      "id": "issue_1",
      "text": "判断の簡潔な記述（50文字以内）",
      "sourceInput": "元の入力テキストから該当箇所を抽出"
    }
  ],
  "dependencies": [
    {
      "from": "issue_1",
      "to": "issue_2",
      "type": "prerequisite" or "related"
    }
  ]
}`;
}

/**
 * Layer 3: 問題/課題切り分け
 */
export function createLayer3Prompt(issues: Array<{ id: string; text: string }>): string {
  const issuesText = issues.map((issue) => `- ${issue.id}: ${issue.text}`).join('\n');

  return `あなたは経営論点を「問題（現象）」と「課題（判断対象）」に再構成する専門家です。

以下の論点リストを分析し、それぞれを問題と課題に変換してください。

# 切り分け基準
- **問題（Problem）**：既に起きている・観測可能な・変更不能な現象
- **課題（Task）**：今後決める・判断可能な・変更可能な対象

# 論点リスト
${issuesText}

# 出力形式
必ず以下のJSON形式で回答してください。Markdown記法は使わず、JSONのみを出力してください。

{
  "problems": [
    {
      "id": "problem_1",
      "description": "現象記述",
      "issueId": "issue_1"
    }
  ],
  "tasks": [
    {
      "id": "task_1",
      "description": "判断対象",
      "problemId": "problem_1",
      "issueId": "issue_1"
    }
  ],
  "conversions": [
    {
      "problemId": "problem_1",
      "taskId": "task_1",
      "reasoning": "問題→課題への変換理由"
    }
  ]
}`;
}

/**
 * 「問えない理由」の抽出 + 判断条件の検出値抽出
 */
export function createNotAskablePrompt(
  inputText: string,
  tasks: Array<{ id: string; description: string }>
): string {
  const tasksText = tasks.map((task) => `- ${task.id}: ${task.description}`).join('\n');

  return `以下の課題が「今のままだと問えない理由」を抽出し、入力に明示されている判断条件の値も抽出してください。

# 課題リスト
${tasksText}

# 元の入力
${inputText}

# 問えない理由のカテゴリ
- owner_unclear: 最終判断主体が不明確（役職名が出るだけでは不十分。「○○が最終決定する」等の明示が必要）
- time_horizon_missing: 具体的な期限が不明確（「今月末」「2週間後」等の特定可能な期限が必要）
- decision_rule_ambiguous: 最優先の判断基準が不明確（複数の基準が並列している場合も該当）
- mixed_issues: 複数論点が混在している

# 判断条件の抽出ルール（厳守）

★★★ 推論・補完・デフォルト値の設定は絶対禁止 ★★★

- owner:
  - 「○○が最終決定する」「○○の承認が必要」等、最終責任者が入力テキストに一字一句明示されている場合のみ抽出
  - 文脈から推測できても、明示されていなければ null
  - 「株主」「取締役」「経営陣」等の一般的な言及は明示とみなさない → null
  - 不明確・曖昧・推測が必要な場合は必ず null

- timeHorizon:
  - 「今月末」「2週間後」「来月中」等、具体的な期限が入力テキストに明示されている場合のみ抽出
  - 「早急に」「できるだけ早く」「近いうちに」等の曖昧な表現は null
  - 不明確・曖昧・推測が必要な場合は必ず null

- decisionRule:
  - 「ROIを最優先」「コスト削減を第一基準」等、最優先基準が入力テキストに明示されている場合のみ抽出
  - 複数の基準が並列している場合（「成長性も利益率も重要」等）は null
  - 課題から推測できる価値観や方向性は抽出しない → null
  - 不明確・曖昧・推測が必要な場合は必ず null

# 抽出してはいけない例（必読）
- 入力：「株主は配当維持を期待」→ owner: null（最終責任者は明示されていない）
- 入力：「取締役会は今月末に結論を求めている」→ owner: null（「結論を求めている」は期限の文脈。最終決定者の明示ではない）
- 入力：「取締役の一部は賛成、創業家は反対」→ owner: null（賛否の言及であり、最終決定者の明示ではない）
- 入力：「経営陣で議論中」→ owner: null（議論の主体であり、最終決定者の明示ではない）
- 入力：「成長鈍化が課題」→ decisionRule: null（判断基準は明示されていない）
- 入力：「判断基準が定まらない」→ decisionRule: null（基準が不明確であることの説明）
- 入力：「来期予算編成の前に決めたい」→ timeHorizon: null（具体的な期日が不明）
- 入力：「取締役会は今月末に結論を求めている」→ timeHorizon: "今月末"（期限の明示 ✅ ownerではなくtimeHorizonとして抽出）

# 出力形式
必ず以下のJSON形式で回答してください。Markdown記法は使わず、JSONのみを出力してください。
extractedConditions の値は、入力テキストに明示されている文字列か null のみ使用してください。

{
  "reasons": [
    {
      "category": "owner_unclear",
      "description": "理由の説明",
      "evidence": "入力テキストから該当箇所を抽出（あれば）"
    }
  ],
  "extractedConditions": {
    "owner": null,
    "timeHorizon": null,
    "decisionRule": null
  }
}`;
}
